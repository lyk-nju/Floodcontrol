from utils.token_frame import FRAMES_PER_TOKEN


GENERATION_MODES = frozenset({"stream", "rolling"})


@dataclass(frozen=True)
class EvaluationPrompt:
    timeline: tuple[str, ...]
    caption: str
    tokens: tuple[str, ...]
    change_frames: np.ndarray


@dataclass(frozen=True)
class GeneratedSequence:
    mode: str
    hybrid_motion: HybridMotion
    root_motion: torch.Tensor
    body_motion: torch.Tensor
    prompt: EvaluationPrompt
    traces: tuple[object, ...]


@dataclass(frozen=True)
class GeneratedSequenceBatch:
    """One equal-length offline T2M batch generated by the stream solver."""

    root_motion: torch.Tensor
    body_motion: torch.Tensor
    prompts: tuple[EvaluationPrompt, ...]


def rotate_evaluation_sample(
    sample: dict[str, object],
    *,
    frame_count: int,
    yaw_degrees: float,
) -> dict[str, object]:
    """Return a root/body-consistent global-yaw variant of one sample."""

    frames = int(frame_count)
    root = sample["root_motion"][:frames]
    body = sample["body_motion"][:frames]
    angle = torch.tensor(
        [math.radians(float(yaw_degrees))],
        device=root.device,
        dtype=root.dtype,
    )
    rotated_root, rotated_body = rotate_motion_yaw(
        root[None],
        body[None].to(device=root.device, dtype=root.dtype),
        angle,
    )
    rotated = dict(sample)
    rotated["root_motion"] = rotated_root[0]
    rotated["body_motion"] = rotated_body[0].to(body)
    return rotated


def create_evaluation_initial_noise(
    module,
    *,
    window_tokens: int,
    seed: int,
    yaw_degrees: float,
) -> HybridMotion:
    """Create paired source noise and apply the physical yaw action to root5."""

    parameter = next(module.model.parameters())
    device = parameter.device
    dtype = parameter.dtype
    generator = torch.Generator(device=device).manual_seed(int(seed))
    root = torch.randn(
        1,
        int(window_tokens),
        FRAMES_PER_TOKEN,
        5,
        device=device,
        dtype=dtype,
        generator=generator,
    )
    latent = torch.randn(
        1,
        int(window_tokens),
        module.model.latent_dim,
        device=device,
        dtype=dtype,
        generator=generator,
    )
    angle = math.radians(float(yaw_degrees))
    if abs(angle) >= 1e-12:
        cosine = math.cos(angle)
        sine = math.sin(angle)
        rotated = root.clone()
        x = root[..., 0]
        z = root[..., 2]
        rotated[..., 0] = cosine * x + sine * z
        rotated[..., 2] = -sine * x + cosine * z
        heading_cosine = root[..., 3]
        heading_sine = root[..., 4]
        rotated[..., 3] = cosine * heading_cosine - sine * heading_sine
        rotated[..., 4] = sine * heading_cosine + cosine * heading_sine
        root = rotated
    return HybridMotion(root, latent)


def compile_evaluation_prompt(
    sample: dict[str, object],
    *,
    frame_count: int,
) -> EvaluationPrompt:
    """Compile one deterministic token prompt timeline from full annotations."""

    token_count = int(frame_count) // FRAMES_PER_TOKEN
    annotations = list(sample.get("text_data", []))
    if not annotations:
        return EvaluationPrompt(
            timeline=("",) * token_count,
            caption="",
            tokens=(),
            change_frames=np.asarray([0, frame_count], dtype=np.int64),
        )

    indexed = []
    for order, annotation in enumerate(annotations):
        start = max(0, int(annotation["start_frame"]))
        end = min(frame_count, int(annotation["end_frame"]))
        if end > start:
            indexed.append((start, end, order, annotation))
    if not indexed:
        return EvaluationPrompt(
            timeline=("",) * token_count,
            caption="",
            tokens=(),
            change_frames=np.asarray([0, frame_count], dtype=np.int64),
        )

    dataset = str(sample.get("dataset", ""))
    if dataset == "HumanML3D":
        selected = max(
            indexed,
            key=lambda item: (
                int(item[0] == 0 and item[1] == frame_count),
                item[1] - item[0],
                -item[2],
            ),
        )[-1]
        text = str(selected["text"])
        return EvaluationPrompt(
            timeline=(text,) * token_count,
            caption=text,
            tokens=tuple(str(token) for token in selected.get("tokens", [])),
            change_frames=np.asarray([0, frame_count], dtype=np.int64),
        )

    timeline: list[str] = []
    selected_annotations: list[dict[str, object] | None] = []
    for token_index in range(token_count):
        token_start = token_index * FRAMES_PER_TOKEN
        token_end = token_start + FRAMES_PER_TOKEN
        candidates = []
        for start, end, order, annotation in indexed:
            overlap = max(0, min(token_end, end) - max(token_start, start))
            if overlap:
                candidates.append((overlap, -(end - start), -order, annotation))
        selected = max(candidates, key=lambda item: item[:-1])[-1] if candidates else None
        selected_annotations.append(selected)
        timeline.append("" if selected is None else str(selected["text"]))

    changes = [0]
    for index in range(1, token_count):
        if timeline[index] != timeline[index - 1]:
            changes.append(index * FRAMES_PER_TOKEN)
    changes.append(frame_count)
    first = next((value for value in selected_annotations if value is not None), None)
    return EvaluationPrompt(
        timeline=tuple(timeline),
        caption="" if first is None else str(first["text"]),
        tokens=tuple(() if first is None else first.get("tokens", [])),
        change_frames=np.asarray(changes, dtype=np.int64),
    )


def _text_encoder(module):
    def encode(texts: list[str], device: torch.device) -> list[torch.Tensor]:
        return [value.to(device=device) for value in module.text_embeddings.lookup(texts)]

    return encode


def _lookup_text_context(
    module,
    texts: list[str],
    *,
    device: torch.device,
) -> list[torch.Tensor]:
    """Move each unique prompt once while retaining tensor identity reuse."""

    unique_texts = list(dict.fromkeys(str(text) for text in texts))
    values = module.text_embeddings.lookup(unique_texts)
    if len(values) != len(unique_texts):
        raise RuntimeError("text embedding lookup returned the wrong number of values")
    unique_values = {
        text: value.to(device=device)
        for text, value in zip(unique_texts, values)
    }
    return [unique_values[str(text)] for text in texts]


def _batched_t2m_condition(
    module,
    prompts: tuple[EvaluationPrompt, ...],
    *,
    window_tokens: int,
    include_initial_xz: bool,
    device: torch.device,
    dtype: torch.dtype,
):
    batch = len(prompts)
    timelines: list[str] = []
    for prompt in prompts:
        if not prompt.timeline:
            raise ValueError("T2M prompt timeline must contain at least one token")
        values = list(prompt.timeline)
        values.extend([values[-1]] * (int(window_tokens) - len(values)))
        timelines.extend(values[: int(window_tokens)])
    text_context = _lookup_text_context(module, timelines, device=device)
    null_context = _lookup_text_context(module, [""], device=device)[0]

    root_value = root_mask = None
    if include_initial_xz:
        root_value = torch.zeros(
            batch,
            int(window_tokens) * FRAMES_PER_TOKEN,
            5,
            device=device,
            dtype=dtype,
        )
        root_mask = torch.zeros_like(root_value, dtype=torch.bool)
        root_mask[:, 0, 0] = True
        root_mask[:, 0, 2] = True
    return create_ldf_condition(
        text_context=text_context,
        text_null_context=[null_context] * batch,
        root_condition_value=root_value,
        root_condition_mask=root_mask,
    )


@torch.no_grad()
def generate_t2m_evaluation_batch(
    module,
    samples: list[dict[str, object]],
    *,
    guidance_mode: str,
    cfg_scale_joint: float,
    seeds: list[int],
    frame_count: int,
    num_denoise_steps: int,
) -> GeneratedSequenceBatch:
    """Generate one equal-length, no-route T2M batch with stream semantics.

    This is deliberately an offline evaluator rather than a replacement for
    :class:`InferenceSession`. Every sample receives the same number of stream
    commits, while source noise is created from its own generator so results do
    not depend on batch composition or ordering.
    """

    if not samples:
        raise ValueError("T2M evaluation batch must contain at least one sample")
    if len(seeds) != len(samples):
        raise ValueError("T2M evaluation seeds must match the sample batch")
    frames = int(frame_count)
    if frames <= 0 or frames % FRAMES_PER_TOKEN:
        raise ValueError("T2M batch frame_count must be a positive multiple of four")
    if int(num_denoise_steps) <= 0:
        raise ValueError("num_denoise_steps must be positive")
    for sample in samples:
        if int(sample["root_motion"].shape[0]) < frames:
            raise ValueError("every T2M batch sample must contain frame_count frames")

    model_parameter = next(module.model.parameters())
    device = model_parameter.device
    dtype = model_parameter.dtype
    vae_parameter = next(module.vae.parameters())
    if vae_parameter.device != device:
        raise ValueError("LDF and BodyVAE must share one device")
    vae_dtype = vae_parameter.dtype
    batch = len(samples)
    token_count = frames // FRAMES_PER_TOKEN
    window_tokens = max(token_count, int(module.model.chunk_size) + 1)
    prompts = tuple(
        compile_evaluation_prompt(sample, frame_count=frames)
        for sample in samples
    )

    root_noise: list[torch.Tensor] = []
    latent_noise: list[torch.Tensor] = []
    for seed in seeds:
        generator = torch.Generator(device=device).manual_seed(int(seed))
        root_noise.append(
            torch.randn(
                1,
                window_tokens,
                FRAMES_PER_TOKEN,
                5,
                device=device,
                dtype=dtype,
                generator=generator,
            )
        )
        latent_noise.append(
            torch.randn(
                1,
                window_tokens,
                module.model.latent_dim,
                device=device,
                dtype=dtype,
                generator=generator,
            )
        )
    initial_noise = HybridMotion(
        torch.cat(root_noise, dim=0),
        torch.cat(latent_noise, dim=0),
    )
    state_generator = torch.Generator(device=device).manual_seed(0)
    state = module.model.init_stream_state(
        batch_size=batch,
        window_tokens=window_tokens,
        device=device,
        dtype=dtype,
        generator=state_generator,
        initial_noise=initial_noise,
        num_denoise_steps=int(num_denoise_steps),
    )
    decoder_state = module.vae.init_decoder_state(
        batch,
        device=device,
        dtype=vae_dtype,
    )
    origin_xz = torch.stack(
        [
            sample["root_motion"][0, [0, 2]].to(device=device, dtype=dtype)
            for sample in samples
        ],
        dim=0,
    )
    previous_root_world: torch.Tensor | None = None

    initial_condition = _batched_t2m_condition(
        module,
        prompts,
        window_tokens=window_tokens,
        include_initial_xz=True,
        device=device,
        dtype=dtype,
    )
    steady_condition = _batched_t2m_condition(
        module,
        prompts,
        window_tokens=window_tokens,
        include_initial_xz=False,
        device=device,
        dtype=dtype,
    )
    root_chunks: list[torch.Tensor] = []
    body_chunks: list[torch.Tensor] = []
    for token_index in range(token_count):
        condition = initial_condition if token_index == 0 else steady_condition
        state, committed = module.model.stream_generate_step(
            state,
            condition,
            roll_window=False,
            cfg_mode=str(guidance_mode),
            cfg_scale_joint=float(cfg_scale_joint),
        )
        world_root = committed.root_motion.clone()
        world_root[..., 0] += origin_xz[:, None, None, 0]
        world_root[..., 2] += origin_xz[:, None, None, 1]
        world_root = project_root_heading(world_root)

        local_root, local_valid = recover_local_root(
            world_root.flatten(1, 2),
            previous_root_world,
            fps=float(module.model.fps),
        )
        decoder_state, body = module.vae.detokenize_step(
            committed.latent_motion.to(device=device, dtype=vae_dtype),
            local_root.to(device=device, dtype=vae_dtype),
            local_valid.to(device=device),
            decoder_state,
        )
        root_chunks.append(world_root[:, 0])
        body_chunks.append(body.body_motion(threshold=0.5))

        translation = committed.root_motion[:, 0, -1, [0, 2]]
        origin_xz = origin_xz + translation.to(origin_xz)
        previous_root_world = world_root[:, -1, -1].clone()

    root_motion = torch.cat(root_chunks, dim=1)
    body_motion = torch.cat(body_chunks, dim=1)
    if tuple(root_motion.shape) != (batch, frames, 5):
        raise RuntimeError("batched T2M generation produced invalid root shape")
    if tuple(body_motion.shape[:2]) != (batch, frames):
        raise RuntimeError("batched T2M generation produced invalid body shape")
    if not bool(torch.isfinite(root_motion).all()) or not bool(
        torch.isfinite(body_motion).all()
    ):
        raise RuntimeError("batched T2M generation produced non-finite motion")
    return GeneratedSequenceBatch(
        root_motion=root_motion,
        body_motion=body_motion,
        prompts=prompts,
    )


@torch.no_grad()
def generate_evaluation_sequence(
    module,
    sample: dict[str, object],
    *,
    mode: str,
    guidance_mode: str | None = None,
    cfg_scale_joint: float | None = None,
    seed: int,
    frame_count: int,
    dense_xz: bool,
    rolling_window_tokens: int,
    max_horizon_token: int,
    num_denoise_steps: int,
    initial_noise_yaw_degrees: float | None = None,
) -> GeneratedSequence:
    """Generate and causally decode one complete physical validation sequence."""

    mode = str(mode)
    if mode not in GENERATION_MODES:
        raise ValueError(f"unsupported evaluation generation mode {mode!r}")
    frames = int(frame_count)
    if frames <= 0 or frames % FRAMES_PER_TOKEN:
        raise ValueError("evaluation frame_count must be a positive multiple of four")
    target_root = sample["root_motion"][:frames]
    if target_root.ndim != 2 or target_root.shape[-1] != 5:
        raise ValueError("evaluation sample root_motion must be [F,5]")
    token_count = frames // FRAMES_PER_TOKEN
    prompt = compile_evaluation_prompt(sample, frame_count=frames)

    if mode == "stream":
        window_tokens = max(token_count, int(module.model.chunk_size) + 1)
        rolling = False
    else:
        window_tokens = int(rolling_window_tokens)
        rolling = True
    if window_tokens <= int(module.model.chunk_size):
        raise ValueError("evaluation window must be larger than the LDF chunk size")
    initial_noise = None
    if initial_noise_yaw_degrees is not None:
        initial_noise = create_evaluation_initial_noise(
            module,
            window_tokens=window_tokens,
            seed=seed,
            yaw_degrees=float(initial_noise_yaw_degrees),
        )

    session = InferenceSession(
        ldf=module.model,
        body_vae=module.vae,
        text_encoder=_text_encoder(module),
        config=InferenceConfig(
            window_tokens=window_tokens,
            max_horizon_token=int(max_horizon_token),
            num_denoise_steps=int(num_denoise_steps),
            rolling=rolling,
        ),
        guidance=GuidanceConfig(
            mode=(
                module.model.cfg_mode
                if guidance_mode is None
                else str(guidance_mode)
            ),
            scale_text=module.model.cfg_scale_text,
            scale_constraint=module.model.cfg_scale_constraint,
            scale_joint=(
                module.model.cfg_scale_joint
                if cfg_scale_joint is None
                else float(cfg_scale_joint)
            ),
        ),
        seed=int(seed),
        initial_world_xz=target_root[0, [0, 2]].tolist(),
        initial_text=prompt.timeline[0],
        initial_noise=initial_noise,
    )
    for token_index in range(1, token_count):
        if prompt.timeline[token_index] != prompt.timeline[token_index - 1]:
            session.update_text(
                prompt.timeline[token_index], effective_token=token_index
            )
    if dense_xz:
        session.update_route(
            times=np.arange(frames, dtype=np.float32) / float(module.model.fps),
            points_xz=target_root[:, [0, 2]].detach().cpu().float().numpy(),
            reference=RouteReference.WORLD,
            end_behavior=RouteEndBehavior.HOLD,
            source="validation_dense_xz",
        )

    chunks = list(session.generate(token_count))
    if len(chunks) != token_count:
        raise RuntimeError("evaluation runtime committed the wrong number of tokens")
    if any(chunk.committed_motion is None for chunk in chunks):
        raise RuntimeError("evaluation runtime did not expose committed hybrid motion")
    root = torch.cat([chunk.root_motion for chunk in chunks], dim=1)[0]
    body = torch.cat(
        [chunk.body_prediction.body_motion(threshold=0.5) for chunk in chunks],
        dim=1,
    )[0]
    if tuple(root.shape[:1]) != (frames,) or tuple(body.shape[:1]) != (frames,):
        raise RuntimeError("evaluation decode did not recover exactly four frames per token")
    hybrid_motion = HybridMotion(
        root.reshape(1, token_count, FRAMES_PER_TOKEN, 5),
        torch.cat(
            [chunk.committed_motion.latent_motion for chunk in chunks], dim=1
        ),
    )
    hybrid_motion.validate()
    return GeneratedSequence(
        mode=mode,
        hybrid_motion=hybrid_motion,
        root_motion=root,
        body_motion=body,
        prompt=prompt,
        traces=tuple(chunk.trace for chunk in chunks),
    )


